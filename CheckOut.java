    private void ViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewDetailsActionPerformed
        customer = validations.getJComboBoxValue(CID);
        String ResNumber = validations.getJComboBoxValue(reservationNumber);
        java.util.Date CheckIN = null,CheckOUT = null;
        double price = 0;
        String d1 = null,d2 = null;
        try{
              if(validations.isEmpty(customer)){
                     validations.EmptyForm();
              }
              else{
                  //get details from reserve table
                  String Rstatus="BOOKED";
                  String getReservationDetailsFormReserve = "SELECT * FROM reserve WHERE RESERVATION_NUMBER = '"+ResNumber+"'";
                  Statement stat = conn.createStatement();
                  ResultSet rs = stat.executeQuery(getReservationDetailsFormReserve);
                  if(rs.next()){
                        roomId.setText(rs.getString(6));
                        roomType.setText(rs.getString("ROOM_TYPE"));
                        checkinDate.setText(rs.getString("CHECKIN_DATE"));
                        CheckIN = (rs.getDate("CHECKIN_DATE"));
                        checkoutDate.setText(rs.getString("CHECKOUT_DATE"));
                        CheckOUT = (rs.getDate("CHECKOUT_DATE"));
                        pricePerDay.setText(rs.getString("PRICE_PER_DAY"));
                        price = rs.getDouble("PRICE_PER_DAY");
                        
                        //total.setText(rs.getString("TOTAL_BILL"));
                        //System.out.println(validations.daysBetween(CheckIN,CheckOUT));
                        int number_of_days = validations.daysBetween(CheckOUT,CheckIN);
                        double totalBill = number_of_days*price;
                        total.setText(Double.toString(totalBill));
                  }
            }
                  
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_ViewDetailsActionPerformed

