    private void CheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckOutActionPerformed
           
            customer = validations.getJComboBoxValue(CID);
            String ResNumber = validations.getJComboBoxValue(reservationNumber);
            String checkin = checkinDate.getText();
           
        
        try{
              if(validations.isEmpty(customer)){
                     validations.EmptyForm();
              }
              else{
                  //get details from reserve table
                  
                     String rtype = roomType.getText();
                     String rid = roomId.getText();
                     String checkOutDateNew = validations.getlabelValue(checkoutDate);
                     
                     //update reserve table
                     
                     String CAHNGE_ROOM_STATUS = "update reserve set status = 'EXPIRED' where RESERVATION_NUMBER = ?";
                     PreparedStatement CheckOutfromRoom = conn.prepareStatement(CAHNGE_ROOM_STATUS);
                     
                     CheckOutfromRoom.setInt(1, Integer.parseInt(ResNumber));
                     CheckOutfromRoom.executeUpdate();
                     
                     //update room table
                     
                     String query_change = "update room set status = 'free',checkoutDate = ?  where  checkinDate = ?  and roomTypeId = ? and roomId = ?";
                     PreparedStatement CAHNGE_ROOM_STATUS_in_rooms = conn.prepareStatement(query_change);
                     
                     CAHNGE_ROOM_STATUS_in_rooms.setString(1, "0000-00-00");
                     CAHNGE_ROOM_STATUS_in_rooms.setString(2, checkin);
                     CAHNGE_ROOM_STATUS_in_rooms.setString(3, rtype);
                     CAHNGE_ROOM_STATUS_in_rooms.setString(4, rid);
                    
                     
                     CAHNGE_ROOM_STATUS_in_rooms.executeUpdate();
                     JOptionPane.showMessageDialog(this, "check Out Succesfull");
                  
                  
                     
             }
                
      }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
                
    }//GEN-LAST:event_CheckOutActionPerformed

